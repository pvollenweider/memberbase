<?php
/**
 * Web installer — first-run setup wizard.
 *
 * Steps:
 *   1  Prerequisites check
 *   2  Database connection + write conf/db.php
 *   3  Schema initialisation (CREATE TABLE IF NOT EXISTS)
 *   4  Admin account creation
 *   done  Redirect to app
 *
 * Guard: if conf/db.php exists AND app_users has an admin → redirect to index.php.
 *
 * @license AGPL-3.0-or-later
 */

declare(strict_types=1);

define('INSTALL_MODE', true);

$confFile   = __DIR__ . '/../conf/db.php';
$schemaFile = __DIR__ . '/../schema.sql';
$step       = $_GET['step'] ?? '1';
$errors     = [];
$success    = null;

// ---------- Guard — already installed ----------
if (file_exists($confFile)) {
    require_once $confFile;
    try {
        $h = defined('DB_HOST') ? DB_HOST : 'localhost';
        $u = defined('DB_USER') ? DB_USER : '';
        $p = defined('DB_PASS') ? DB_PASS : '';
        $d = defined('DB_NAME') ? DB_NAME : '';
        $chk = new PDO("mysql:host=$h;dbname=$d;charset=utf8mb4", $u, $p, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);
        $adminCount = (int)$chk->query("SELECT COUNT(*) FROM app_users WHERE role='admin' AND is_active=1")->fetchColumn();
        if ($adminCount > 0) {
            header('Location: index.php');
            exit;
        }
        // DB configured but no admin yet — skip to step 4
        if ($step === '1' || $step === '2' || $step === '3') {
            header('Location: install.php?step=4');
            exit;
        }
    } catch (Exception) {
        // conf exists but connection fails — continue installer
    }
}

// ---------- Step handlers ----------

// Step 2 POST — test connection + write conf/db.php
if ($_SERVER['REQUEST_METHOD'] === 'POST' && $step === '2') {
    $dbHost = trim($_POST['db_host'] ?? 'localhost');
    $dbPort = (int)($_POST['db_port'] ?? 3306);
    $dbName = trim($_POST['db_name'] ?? '');
    $dbUser = trim($_POST['db_user'] ?? '');
    $dbPass = $_POST['db_pass'] ?? '';

    if (!$dbName) $errors[] = 'Nom de la base de données requis.';
    if (!$dbUser) $errors[] = 'Utilisateur requis.';

    if (!$errors) {
        try {
            $dsn = "mysql:host={$dbHost};port={$dbPort};dbname={$dbName};charset=utf8mb4";
            new PDO($dsn, $dbUser, $dbPass, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);

            // Write conf/db.php
            $confDir = dirname($confFile);
            if (!is_dir($confDir)) {
                mkdir($confDir, 0750, true);
            }
            $confContent = sprintf(
                "<?php\n// Generated by installer — %s\ndefine('DB_HOST', %s);\ndefine('DB_PORT', %d);\ndefine('DB_NAME', %s);\ndefine('DB_USER', %s);\ndefine('DB_PASS', %s);\n",
                date('Y-m-d H:i:s'),
                var_export($dbHost, true),
                $dbPort,
                var_export($dbName, true),
                var_export($dbUser, true),
                var_export($dbPass, true)
            );
            if (file_put_contents($confFile, $confContent) === false) {
                $errors[] = "Impossible d'écrire <code>{$confFile}</code>. Vérifiez les permissions du répertoire <code>conf/</code>.";
            } else {
                header('Location: install.php?step=3');
                exit;
            }
        } catch (PDOException $e) {
            $errors[] = 'Connexion échouée : ' . htmlspecialchars($e->getMessage(), ENT_QUOTES, 'UTF-8');
        }
    }
}

// Step 3 POST — run schema
if ($_SERVER['REQUEST_METHOD'] === 'POST' && $step === '3') {
    if (!file_exists($confFile)) { header('Location: install.php?step=2'); exit; }
    if (!file_exists($schemaFile)) { $errors[] = "Fichier schema.sql introuvable ({$schemaFile})."; }

    if (!$errors) {
        require_once $confFile;
        try {
            $h   = defined('DB_HOST') ? DB_HOST : 'localhost';
            $prt = defined('DB_PORT') ? DB_PORT : 3306;
            $u   = defined('DB_USER') ? DB_USER : '';
            $p   = defined('DB_PASS') ? DB_PASS : '';
            $d   = defined('DB_NAME') ? DB_NAME : '';
            $pdo = new PDO("mysql:host=$h;port=$prt;dbname=$d;charset=utf8mb4", $u, $p, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);

            $sql = file_get_contents($schemaFile);
            // Split on ; + newline, skip comments and empty lines
            $statements = array_filter(
                array_map('trim', preg_split('/;\s*\n/', $sql)),
                fn($s) => $s !== '' && !str_starts_with($s, '--')
            );
            foreach ($statements as $stmt) {
                $pdo->exec($stmt);
            }
            header('Location: install.php?step=4');
            exit;
        } catch (PDOException $e) {
            $errors[] = 'Erreur SQL : ' . htmlspecialchars($e->getMessage(), ENT_QUOTES, 'UTF-8');
        }
    }
}

// Step 4 POST — create admin user
if ($_SERVER['REQUEST_METHOD'] === 'POST' && $step === '4') {
    if (!file_exists($confFile)) { header('Location: install.php?step=2'); exit; }

    $username    = trim($_POST['username'] ?? '');
    $displayName = trim($_POST['display_name'] ?? '');
    $email       = trim($_POST['email'] ?? '');
    $password    = $_POST['password'] ?? '';
    $password2   = $_POST['password2'] ?? '';

    if (!$username)            $errors[] = 'Identifiant requis.';
    if (!preg_match('/^[a-zA-Z0-9._-]{2,50}$/', $username)) $errors[] = 'Identifiant invalide (2–50 caractères, lettres/chiffres/.-_).';
    if (strlen($password) < 8) $errors[] = 'Mot de passe trop court (min. 8 caractères).';
    if ($password !== $password2) $errors[] = 'Les mots de passe ne correspondent pas.';

    if (!$errors) {
        require_once $confFile;
        try {
            $h   = defined('DB_HOST') ? DB_HOST : 'localhost';
            $prt = defined('DB_PORT') ? DB_PORT : 3306;
            $u   = defined('DB_USER') ? DB_USER : '';
            $p   = defined('DB_PASS') ? DB_PASS : '';
            $d   = defined('DB_NAME') ? DB_NAME : '';
            $pdo = new PDO("mysql:host=$h;port=$prt;dbname=$d;charset=utf8mb4", $u, $p, [PDO::ATTR_ERRMODE => PDO::ERRMODE_EXCEPTION]);

            $hash = password_hash($password, PASSWORD_DEFAULT);
            $stmt = $pdo->prepare(
                "INSERT INTO app_users (username, display_name, email, password_hash, role, force_password_change)
                 VALUES (?, ?, ?, ?, 'admin', 0)"
            );
            $stmt->execute([$username, $displayName ?: $username, $email ?: null, $hash]);
            header('Location: install.php?step=done');
            exit;
        } catch (PDOException $e) {
            if (str_contains($e->getMessage(), 'Duplicate')) {
                $errors[] = "L'identifiant «{$username}» est déjà utilisé.";
            } else {
                $errors[] = 'Erreur : ' . htmlspecialchars($e->getMessage(), ENT_QUOTES, 'UTF-8');
            }
        }
    }
}

// ---------- Prerequisites check ----------
$prereqs = [];
$prereqs[] = ['label' => 'PHP ≥ 8.1', 'ok' => PHP_VERSION_ID >= 80100, 'detail' => 'PHP ' . PHP_VERSION];
$prereqs[] = ['label' => 'Extension PDO MySQL', 'ok' => extension_loaded('pdo_mysql'), 'detail' => extension_loaded('pdo_mysql') ? 'OK' : 'Manquante'];
$prereqs[] = ['label' => 'Extension mbstring', 'ok' => extension_loaded('mbstring'), 'detail' => extension_loaded('mbstring') ? 'OK' : 'Manquante'];
$prereqs[] = ['label' => 'Écriture dans conf/', 'ok' => is_writable(dirname($confFile)) || !file_exists(dirname($confFile)), 'detail' => is_writable(dirname($confFile)) ? 'OK' : (is_dir(dirname($confFile)) ? 'Non accessible en écriture' : 'Répertoire inexistant')];
$prereqsOk = array_reduce($prereqs, fn($carry, $p) => $carry && $p['ok'], true);

// ---------- Existing DB values for prefill ----------
$prefill = ['db_host' => 'localhost', 'db_port' => '3306', 'db_name' => '', 'db_user' => ''];
if (file_exists($confFile)) {
    require_once $confFile;
    $prefill['db_host'] = defined('DB_HOST') ? DB_HOST : $prefill['db_host'];
    $prefill['db_port'] = defined('DB_PORT') ? (string)DB_PORT : $prefill['db_port'];
    $prefill['db_name'] = defined('DB_NAME') ? DB_NAME : $prefill['db_name'];
    $prefill['db_user'] = defined('DB_USER') ? DB_USER : $prefill['db_user'];
}
foreach ($prefill as $k => $v) {
    $prefill[$k] = htmlspecialchars($v, ENT_QUOTES, 'UTF-8');
}
