  </div>

  <!-- Progress -->
  <?php
  $steps = ['1' => 'Prérequis', '2' => 'Base de données', '3' => 'Schéma', '4' => 'Compte admin'];
  $currentIdx = array_search($step, array_keys($steps));
  ?>
  <?php if ($step !== 'done'): ?>
  <div class="d-flex gap-2 mb-4">
    <?php foreach ($steps as $s => $label): ?>
    <?php
      $idx = array_search($s, array_keys($steps));
      $cls = $idx < $currentIdx ? 'bg-success text-white' : ($s === $step ? 'bg-primary text-white' : 'bg-light text-muted');
    ?>
    <div class="flex-fill text-center rounded p-2 small fw-semibold <?= $cls ?>">
      <?= (string)($idx + 1) ?>. <?= $label ?>
    </div>
    <?php endforeach ?>
  </div>
  <?php endif ?>

  <?php if ($errors): ?>
  <div class="alert alert-danger">
    <ul class="mb-0 ps-3">
      <?php foreach ($errors as $e): ?>
      <li><?= $e ?></li>
      <?php endforeach ?>
    </ul>
  </div>
  <?php endif ?>

  <!-- ===== STEP 1 — Prerequisites ===== -->
  <?php if ($step === '1'): ?>
  <div class="card shadow-sm">
    <div class="card-body">
      <h2 class="h5 mb-3">Prérequis</h2>
      <div class="mb-3">
        <?php foreach ($prereqs as $pr): ?>
        <div class="prereq-row">
          <span><?= $pr['label'] ?></span>
          <span class="<?= $pr['ok'] ? 'text-success fw-semibold' : 'text-danger fw-semibold' ?>">
            <?= $pr['ok'] ? '✓' : '✗' ?> <?= htmlspecialchars($pr['detail'], ENT_QUOTES, 'UTF-8') ?>
          </span>
        </div>
        <?php endforeach ?>
      </div>
      <?php if ($prereqsOk): ?>
      <a href="install.php?step=2" class="btn btn-primary w-100">Continuer</a>
      <?php else: ?>
      <div class="alert alert-warning mb-2 small">Corrigez les prérequis manquants avant de continuer.</div>
      <button class="btn btn-secondary w-100" disabled>Continuer</button>
      <?php endif ?>
    </div>
  </div>

  <!-- ===== STEP 2 — DB Connection ===== -->
  <?php elseif ($step === '2'): ?>
  <div class="card shadow-sm">
    <div class="card-body">
      <h2 class="h5 mb-1">Connexion à la base de données</h2>
      <p class="text-muted small mb-3">Les paramètres seront enregistrés dans <code>conf/db.php</code>.</p>
      <form method="post" action="install.php?step=2">
        <div class="row g-2 mb-2">
          <div class="col-8">
            <label class="form-label small fw-semibold" for="db_host">Hôte</label>
            <input type="text" class="form-control form-control-sm" id="db_host" name="db_host"
                   value="<?= $prefill['db_host'] ?>" required>
          </div>
          <div class="col-4">
            <label class="form-label small fw-semibold" for="db_port">Port</label>
            <input type="number" class="form-control form-control-sm" id="db_port" name="db_port"
                   value="<?= $prefill['db_port'] ?>" required>
          </div>
        </div>
        <div class="mb-2">
          <label class="form-label small fw-semibold" for="db_name">Nom de la base</label>
          <input type="text" class="form-control form-control-sm" id="db_name" name="db_name"
                 value="<?= $prefill['db_name'] ?>" placeholder="members" required>
        </div>
        <div class="mb-2">
          <label class="form-label small fw-semibold" for="db_user">Utilisateur</label>
          <input type="text" class="form-control form-control-sm" id="db_user" name="db_user"
                 value="<?= $prefill['db_user'] ?>" placeholder="members" required autocomplete="username">
        </div>
        <div class="mb-3">
          <label class="form-label small fw-semibold" for="db_pass">Mot de passe</label>
          <input type="password" class="form-control form-control-sm" id="db_pass" name="db_pass"
                 autocomplete="current-password">
        </div>
        <button type="submit" class="btn btn-primary w-100">Tester et enregistrer</button>
      </form>
    </div>
  </div>

  <!-- ===== STEP 3 — Schema ===== -->
  <?php elseif ($step === '3'): ?>
  <div class="card shadow-sm">
    <div class="card-body">
      <h2 class="h5 mb-1">Initialisation du schéma</h2>
      <p class="text-muted small mb-3">
        Création des tables depuis <code>schema.sql</code>.
        Les tables existantes ne seront pas modifiées (<code>CREATE TABLE IF NOT EXISTS</code>).
      </p>
      <form method="post" action="install.php?step=3">
        <button type="submit" class="btn btn-primary w-100">Créer les tables</button>
      </form>
    </div>
  </div>

  <!-- ===== STEP 4 — Admin user ===== -->
  <?php elseif ($step === '4'): ?>
  <div class="card shadow-sm">
    <div class="card-body">
      <h2 class="h5 mb-1">Compte administrateur</h2>
      <p class="text-muted small mb-3">Ce compte sera le premier admin de l'application.</p>
      <form method="post" action="install.php?step=4">
        <div class="mb-2">
          <label class="form-label small fw-semibold" for="username">Identifiant</label>
          <input type="text" class="form-control form-control-sm" id="username" name="username"
                 value="<?= htmlspecialchars($_POST['username'] ?? '', ENT_QUOTES, 'UTF-8') ?>"
                 placeholder="admin" required autocomplete="username"
                 pattern="[a-zA-Z0-9._-]{2,50}">
          <div class="form-text">2–50 caractères, lettres/chiffres/.-_</div>
        </div>
        <div class="mb-2">
          <label class="form-label small fw-semibold" for="display_name">Nom affiché</label>
          <input type="text" class="form-control form-control-sm" id="display_name" name="display_name"
                 value="<?= htmlspecialchars($_POST['display_name'] ?? '', ENT_QUOTES, 'UTF-8') ?>"
                 placeholder="Administrateur">
        </div>
        <div class="mb-2">
          <label class="form-label small fw-semibold" for="email">E-mail <span class="text-muted fw-normal">(optionnel)</span></label>
          <input type="email" class="form-control form-control-sm" id="email" name="email"
                 value="<?= htmlspecialchars($_POST['email'] ?? '', ENT_QUOTES, 'UTF-8') ?>">
        </div>
        <div class="mb-2">
          <label class="form-label small fw-semibold" for="password">Mot de passe</label>
          <input type="password" class="form-control form-control-sm" id="password" name="password"
                 required minlength="8" autocomplete="new-password">
          <div class="form-text">Minimum 8 caractères.</div>
        </div>
        <div class="mb-3">
          <label class="form-label small fw-semibold" for="password2">Confirmer le mot de passe</label>
          <input type="password" class="form-control form-control-sm" id="password2" name="password2"
                 required autocomplete="new-password">
